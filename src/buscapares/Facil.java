    public void cartaArriba(Carta carta)
    {
        int op = carta.getN_imagen();
        switch(op){
            case 1:
                ImageIcon imagen1 = new ImageIcon("src/imagenes/dragon_ojos_azules.png");
                carta.getCarta().setIcon(imagen1);
                break;
            case 2:
                ImageIcon imagen2 = new ImageIcon("src/imagenes/Exodia.png");
                carta.getCarta().setIcon(imagen2);
                break;
            case 3:
                ImageIcon imagen3 = new ImageIcon("src/imagenes/Dragon_negro_ojos_rojos.png");
                carta.getCarta().setIcon(imagen3);
                break;
            case 4:
                ImageIcon imagen4 = new ImageIcon("src/imagenes/Mago_Oscuro.png");
                carta.getCarta().setIcon(imagen4);
                break;
        }
    }
    
    public void comparar(Carta carta)
    {
        for (Carta carta1 : cartas) {
                if(carta1 != carta && carta1.getCarta().isSelected() && carta1.getN_imagen() == carta.getN_imagen())
                {
                        carta1.getCarta().setEnabled(false);
                        carta1.getCarta().setSelected(false);
                        carta.getCarta().setEnabled(false);
                        carta.getCarta().setSelected(false);
                        ganar();
                }
        }
        deseleccionar();
        seleccionados = 0;
    }
    
    public void ganar()
    {
        int bloqueados = 0;
        for(int i = 0; i < cartas.size(); i++)
        {
                if(!cartas.get(i).getCarta().isEnabled())
                {
                        bloqueados++;
                }
        }
        
        if(bloqueados == 8)
        {
                JOptionPane.showMessageDialog(null, "Ganaste");
                setVisible(false);
                new Juego().setVisible(true);
        }
    }
    
    public void deseleccionar(){
        for (Carta carta : cartas) 
        {
                if(carta.getCarta().isEnabled())
                {
                        carta.getCarta().setIcon(defaults);
                        carta.getCarta().setSelected(false);
                }
        }
    }

