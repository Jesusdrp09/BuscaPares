    private void agregarPares()       //Agrega las imagenes en pares
    {
        int par = 0;
        for(int i = 1; i <= 4; i++)
        {
                do{
                        int n = (int) (Math.random()*8);
                        if(cartas.get(n).getN_imagen() == 0)
                        {
                                cartas.get(n).setN_imagen(i);
                                par++;
                        }
                }while(par != 2);
                par = 0;
        }
    }
    
