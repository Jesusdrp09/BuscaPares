        {
                switch(seleccionados)
                {
                        case 0:
                                seleccionados++;
                                cartaArriba(cartas.get(0));
                            break;
                        case 1:
                                seleccionados++;
                                cartaArriba(cartas.get(0));
                                comparar(cartas.get(0));
                            break;
                        case 2:
                                Carta0.setSelected(false);
                                Carta0.setIcon(defaults);
                            break;
                }
        }else{
                if(seleccionados != 0){
                        seleccionados--;
                }
                Carta0.setIcon(defaults);
        }
